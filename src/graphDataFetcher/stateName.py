def stateNameData(stateName: str):
    """
    Args:
        stateName (str): sem state code for graph plotting
    Returns:
        Name of the state for graph plotting!!!
    """
    if stateName == "DN1":
        return "दादरा नगर हवेली"
    elif stateName == "DD1":
        return "दमन दीव"
    elif stateName == "GO1":
        return "गोवा"
    elif stateName == "CS1":
        return "छत्तीसगढ"
    elif stateName == "MP2":
        return "मध्य प्रदेश"
    elif stateName == "GU2":
        return "गुजरात"
    elif stateName == "HZ1":
        return "हजीरा"
    elif stateName == "BR1":
        return "BARC"
    elif stateName == "MH2":
        return "महाराष्ट्र"
    elif stateName == "NR1":
        return "उत्तरी क्षेत्र"
    elif stateName == "ER1":
        return "पूर्वी क्षेत्र"
    elif stateName == "SR1":
        return "दक्षिणी क्षेत्र"