def stateNameData(stateName: str):
    """
    Args:
        stateName (str): sem state code for graph plotting
    Returns:
        Name of the state for graph plotting!!!
    """
    if stateName == "DN1":
        return "Dadra & Nagar Haveli"
    elif stateName == "DD1":
        return "DAMAN & DIU"
    elif stateName == "GO1":
        return "GOA"
    elif stateName == "CS1":
        return "CHHATTISGARH"
    elif stateName == "MP2":
        return "MADHYA PRADESH"
    elif stateName == "GU2":
        return "GUJRAT"
    elif stateName == "HZ1":
        return "HAZIRA"
    elif stateName == "BR1":
        return "BARC"
    elif stateName == "MH2":
        return "MAHARASHTRA"
    elif stateName == "NR1":
        return "NORTHERN REGION"
    elif stateName == "ER1":
        return "EASTERN REGION"
    elif stateName == "SR1":
        return "SOUTHERN REGION"